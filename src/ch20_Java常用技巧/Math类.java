package ch20_Java常用技巧;

import java.lang.Math;    // 在这个包

public class Math类 {
    public static void main(String[] args) {

        // 常用方法：
        System.out.println( Math.round(3.14));  // 四舍五入
        System.out.println((10+5) * (10.0/3));  // 注意运算符优先级
        System.out.println(Math.max(111,222));  // 取最大值
        System.out.println(Math.min(111,222));
        System.out.println(Math.floor(10.0/3));// 保留整数部分
        System.out.println(Math.pow(3,4));  //
        System.out.println(Math.sqrt(120));  // 平方根
        System.out.println(Math.round(3.75*10/10.0)); // 保留小数点后一位
        System.out.println();
        System.out.println();



    }
}
